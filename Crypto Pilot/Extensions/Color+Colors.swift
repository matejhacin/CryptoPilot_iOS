import Foundation
import SwiftUI

extension Color {
    
    // MARK: Primary
    
    static func blakish(_ withAlphaComponent: Double = 1.0) -> Color {
        return Color("Blakish")
    }
    
    static func blue(_ withAlphaComponent: Double = 1.0) -> Color {
        return Color("Blue")
    }
    
    static func darkGray(_ withAlphaComponent: Double = 1.0) -> Color {
        return Color("DarkGray")
    }
    
    static func gray(_ withAlphaComponent: Double = 1.0) -> Color {
        return Color("Gray")
    }
    
    static func white(_ withAlphaComponent: Double = 1.0) -> Color {
        return Color("White")
    }
    
    // MARK: Secondary
    
    static func altBlue(_ withAlphaComponent: Double = 1.0) -> Color {
        return Color("AltBlue")
    }
    
    static func altGray(_ withAlphaComponent: Double = 1.0) -> Color {
        return Color("AltGray")
    }
    
    static func green(_ withAlphaComponent: Double = 1.0) -> Color {
        return Color("Green")
    }
    
    static func lightBlue(_ withAlphaComponent: Double = 1.0) -> Color {
        return Color("LightBlue")
    }
    
    static func red(_ withAlphaComponent: Double = 1.0) -> Color {
        return Color("Red")
    }
    
}
